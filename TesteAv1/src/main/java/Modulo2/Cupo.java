
package Modulo2;


public class Cupo {

    
    public static int ladoArea(int valor){
        return valor * valor;
    }
    
    public static int latealArea(int valor){
        return 4 * (valor * valor);
    }
    
    public static int totalArea(int valor){
        return 6 * (valor * valor);
    }
    
    public static int volumeArea(int valor){
        return (valor * valor) * valor;
    }
}
